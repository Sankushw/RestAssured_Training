package pack2a_Programming;

public class p2_MultiplyTable {
	//Print multiplication table of 5 without using * operator
	public static void main(String[] args) {
		int num=5;
		int total=0;
		for(int i=0;i<10;i++)
		{
			total=total+num;
			System.out.println(total);			
		}

	}


}
